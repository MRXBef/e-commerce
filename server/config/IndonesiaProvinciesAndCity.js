export function getJarak(provinsiA, provinsiB) {
  const relasi = jarakAntarProvinsi.find((rel) => {
    return (
      rel.provinsiIdTerkait.includes(provinsiA) &&
      rel.provinsiIdTerkait.includes(provinsiB)
    );
  });

  return relasi ? relasi.jarak : null;
}
